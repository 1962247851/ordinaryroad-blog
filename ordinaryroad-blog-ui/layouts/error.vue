<template>
  <v-container fluid class="pa-0 fill-height">
    <div v-if="error.statusCode === 404 ">
      <NotFound />
    </div>
    <div v-else>
      <p>{{ otherError }}</p>
    </div>
  </v-container>
</template>

<script>
export default {
  layout: 'empty',
  props: {
    error: {
      type: Object,
      default: null
    }
  },
  data () {
    return {
      pageNotFound: '404 Not Found',
      otherError: '应用发生错误异常'
    }
  },
  head () {
    const title =
      this.error.statusCode === 404 ? this.pageNotFound : this.otherError
    return {
      title
    }
  }
}
</script>

<style scoped>
h1 {
  font-size: 20px;
}
</style>
